package in.wtc.ui.model;

public class DoctorSummary {
	
	private String doctorId;
	private String doctorName;
	private String gender;
	private String knownLanguages;
	private String department;
	private String especiality;
	private String roomNo;
	private String consultationCharge;
	private String parmanent;
	
	public DoctorSummary() {
	}
	public String getDoctorId() {
		return doctorId;
	}
	public void setDoctorId(String doctorId) {
		this.doctorId = doctorId;
	}
	public String getDoctorName() {
		return doctorName;
	}
	public void setDoctorName(String doctorName) {
		this.doctorName = doctorName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getKnownLanguages() {
		return knownLanguages;
	}
	public void setKnownLanguages(String knownLanguages) {
		this.knownLanguages = knownLanguages;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getEspeciality() {
		return especiality;
	}
	public void setEspeciality(String especiality) {
		this.especiality = especiality;
	}
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	public String getConsultationCharge() {
		return consultationCharge;
	}
	public void setConsultationCharge(String consultationCharge) {
		this.consultationCharge = consultationCharge;
	}
	public String getParmanent() {
		return parmanent;
	}
	public void setParmanent(String parmanent) {
		this.parmanent = parmanent;
	}

}
