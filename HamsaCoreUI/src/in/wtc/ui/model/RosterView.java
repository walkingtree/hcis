package in.wtc.ui.model;

public class RosterView {
	
	private String formMonth;
	private String toMonth;
	private String formYear;
	private String toYear;
	private String formWeek;
	private String toWeek;
	private String period;
	public RosterView() {
		super();
	}
	/**
	 * @return the formMonth
	 */
	public String getFormMonth() {
		return formMonth;
	}
	/**
	 * @param formMonth the formMonth to set
	 */
	public void setFormMonth(String formMonth) {
		this.formMonth = formMonth;
	}
	/**
	 * @return the toMonth
	 */
	public String getToMonth() {
		return toMonth;
	}
	/**
	 * @param toMonth the toMonth to set
	 */
	public void setToMonth(String toMonth) {
		this.toMonth = toMonth;
	}
	/**
	 * @return the formYear
	 */
	public String getFormYear() {
		return formYear;
	}
	/**
	 * @param formYear the formYear to set
	 */
	public void setFormYear(String formYear) {
		this.formYear = formYear;
	}
	/**
	 * @return the toYear
	 */
	public String getToYear() {
		return toYear;
	}
	/**
	 * @param toYear the toYear to set
	 */
	public void setToYear(String toYear) {
		this.toYear = toYear;
	}
	/**
	 * @return the formWeek
	 */
	public String getFormWeek() {
		return formWeek;
	}
	/**
	 * @param formWeek the formWeek to set
	 */
	public void setFormWeek(String formWeek) {
		this.formWeek = formWeek;
	}
	/**
	 * @return the toWeek
	 */
	public String getToWeek() {
		return toWeek;
	}
	/**
	 * @param toWeek the toWeek to set
	 */
	public void setToWeek(String toWeek) {
		this.toWeek = toWeek;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}

}
